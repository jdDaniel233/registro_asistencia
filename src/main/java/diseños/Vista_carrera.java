    public void Insertar(JTextField paramId,JTextField paramNombre){
       setId(paramId.getText());
       setNombre(paramNombre.getText());
       
       Conexion con = new Conexion();
       
        
        String consulta="call sp_carrera_insert(?,?);";
        try{
            CallableStatement cs = con.estableceConexion().prepareCall(consulta);
            cs.setString(1,getId());
            cs.setString(2,getNombre());
                       
            cs.execute();
            
            JOptionPane.showMessageDialog(null,"SE INSERTO CORRECTAMENTE LA CARRERA");
            
        }catch (HeadlessException | SQLException e){ 
            JOptionPane.showMessageDialog(null,"NO SE INSERTO CORRECTAMENTE LA CARRERA" + e.toString());
        }
    
    }

