    public void MostrarCarreras(JTable paramCarreras){
    
        Conexion con = new Conexion();
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        TableRowSorter<TableModel> OrdenarTabla = new TableRowSorter<TableModel>(modelo);
        
        paramCarreras.setRowSorter(OrdenarTabla);
        
        String sql="";
        
        
         modelo.addColumn("ID");
         modelo.addColumn("Nombre");
         
         
         
            paramCarreras.setModel(modelo);
            
         sql="call registro_asistencia.sp_carrera_select();";
         
         String[] datos= new String [2];
         Statement st;
          try{
              st=con.estableceConexion().createStatement();
              ResultSet rs = st.executeQuery(sql);
              
              while(rs.next()){
                  datos[0]=rs.getString(1);
                  datos[1]=rs.getString(2);
                  
                  
                  modelo.addRow(datos);
              }
                  paramCarreras.setModel(modelo);
              
              
          }catch (Exception e){
              JOptionPane.showMessageDialog(null, "NO se pudo mostrar los registros, error: "+ e.toString());
          }
    
    
    }

