    public void SeleccionarCarrera(JTable paramCarrera,JTextField paramId, JTextField paramNombre){
        try{
            int fila= paramCarrera.getSelectedRow();
            
            if (fila>=0){
                paramId.setText((paramCarrera.getValueAt(fila,0).toString()));
                paramNombre.setText((paramCarrera.getValueAt(fila,1).toString()));
            }
            else{
                JOptionPane.showMessageDialog(null, "Fila no selecionada");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error de seleccion, erro: " + e.toString());
        }
    
    }
    
