    public void mostrarAsiR(JTable paramAsis){
       
        Conexion con = new Conexion();
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        TableRowSorter<TableModel> OrdenarTabla = new TableRowSorter<TableModel>(modelo);
        
        paramAsis.setRowSorter(OrdenarTabla);
        
        String sql="";
        
        
         modelo.addColumn("Cedula");         
         modelo.addColumn("Nombres");
         modelo.addColumn("Fecha");
         modelo.addColumn("Hora entrada");
         modelo.addColumn("Hora de salida");
         modelo.addColumn("Hora entrada almuerzo");
         modelo.addColumn("Hora salir almuerzo");
         
         
            paramAsis.setModel(modelo);
            
         sql="call registro_asistencia.sp_reporte_asistencias();";
         
         String[] datos= new String [7];
         Statement st;
          try{
              st=con.estableceConexion().createStatement();
              ResultSet rs = st.executeQuery(sql);
              
             // st.setString(1,getCedula());
            //st.setString(2,getFecha());
              
              while(rs.next()){
                  datos[0]=rs.getString(1);
                  datos[1]=rs.getString(2);
                  datos[2]=rs.getString(3);
                  datos[3]=rs.getString(4);
                  datos[4]=rs.getString(5);
                  datos[5]=rs.getString(6);
                  datos[6]=rs.getString(7);
                  
                  
                  modelo.addRow(datos);
              }
                  paramAsis.setModel(modelo);
              
              
          }catch (Exception e){
              JOptionPane.showMessageDialog(null, "NO se pudo mostrar los registros, error: "+ e.toString());
          }
    }
    
    
