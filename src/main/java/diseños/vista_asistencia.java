    public void busqueda(JTextField cedula,JTextField date,JTextField id,JTextField nombre, JTextField fecha,JTextField entrada,JTextField salida,JTextField almue,JTextField almus ){
        String conuslta="call registro_asistencia.sp_buscar_asistencias(?,?);";
        Conexion con = new Conexion();
        try{
            CallableStatement cs=con.estableceConexion().prepareCall(conuslta);
            cs.setString(1, cedula.getText());
            cs.setString(2, date.getText());
            cs.execute();
            ResultSet rs=cs.executeQuery();
            
            if(rs.next()){
               // JOptionPane.showMessageDialog(null, "Registro encontrado ");
                id.setText(rs.getString("doc_id"));
                nombre.setText(rs.getString("doc_nombres"));
                fecha.setText(rs.getString("asi_fecha"));
                entrada.setText(rs.getString("asi_horaEntrada"));
                salida.setText(rs.getString("asi_horaSalida"));
                almus.setText(rs.getString("asi_salirAlmuerzo"));
                almue.setText(rs.getString("asi_entradaAlmuerzo"));
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Registro no encontrado ");
                id.setText("");
                nombre.setText("");
                fecha.setText("");
                entrada.setText("");
                salida.setText("");
                almus.setText("");
                almue.setText("");
            }
        }
        catch(SQLException ex){
        JOptionPane.showMessageDialog(null, "error: "+ex.toString());
        }
    }
    
    public DefaultTableModel BuscarTotal(String buscar){
        String[] columnas={"Cedula","Nombres","Fecha","Hora entrada","Hora Salida","Hora entrada almuerzo","Hora salir almuerzo"};
        String [] registros=new String[7];
        DefaultTableModel modelo= new DefaultTableModel(null,columnas);
        String sql="call registro_asistencia.sp_asistencia_cedula('"+buscar+"');";
        
        Connection cn =null;
        PreparedStatement pst=null;
        ResultSet rs=null;
        
        try{
            Conexion con = new Conexion();
            cn =con.estableceConexion();
            pst=cn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                registros[0]=rs.getString("doc_id");
                registros[1]=rs.getString("doc_nombres");
                registros[2]=rs.getString("asi_fecha");
                registros[3]=rs.getString("asi_horaEntrada");
                registros[4]=rs.getString("asi_horaSalida");
                registros[5]=rs.getString("asi_entradaAlmuerzo");
                registros[6]=rs.getString("asi_salirAlmuerzo");
                modelo.addRow(registros);
                
            }
                    
            
        
        }catch (SQLException ex){
            System.out.print(ex);
        }
        return modelo;
    
        
    }

