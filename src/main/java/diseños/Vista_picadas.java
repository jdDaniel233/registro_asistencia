    public void MostrarPicadas(JTable paramPicadas){
    
        Conexion con = new Conexion();
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        TableRowSorter<TableModel> OrdenarTabla = new TableRowSorter<TableModel>(modelo);
        
        paramPicadas.setRowSorter(OrdenarTabla);
        
        String sql="";
        
        
         modelo.addColumn("ID");         
         modelo.addColumn("Hora Salida");
         modelo.addColumn("Hora Entrada");
         modelo.addColumn("Inicio Almuerzo");
         modelo.addColumn("Final Almuerzo");
         modelo.addColumn("Salida de emergencia");
         modelo.addColumn("Cedula");
         modelo.addColumn("Fecha");
         
            paramPicadas.setModel(modelo);
            
         String id= DocenteL.cedu;
         sql="call registro_asistencia.sp_picadas('"+id+"');";
         
         String[] datos= new String [8];
         Statement st;
          try{
              st=con.estableceConexion().createStatement();
              ResultSet rs = st.executeQuery(sql);
              
              while(rs.next()){
                  datos[0]=rs.getString(1);
                  datos[1]=rs.getString(2);
                  datos[2]=rs.getString(3);
                  datos[3]=rs.getString(4);
                  datos[4]=rs.getString(5);
                  datos[5]=rs.getString(6);
                  datos[6]=rs.getString(7);
                  datos[7]=rs.getString(8);
                  
                  modelo.addRow(datos);
              }
                  paramPicadas.setModel(modelo);
              
              
          }catch (Exception e){
              JOptionPane.showMessageDialog(null, "NO se pudo mostrar los registros, error: "+ e.toString());
          }
    
    
    }

